// Funciones auxiliares de búsqueda

export const getLinksByCategory = (category) => {
    return links.filter(link => link.category === category);
};

export const getLinksByTag = (tag) => {
    return links.filter(link => link.tags.includes(tag.toLowerCase()));
};

export const searchLinks = (searchTerm) => {
    const term = searchTerm.toLowerCase();
    return links.filter(link => 
        link.title.toLowerCase().includes(term) ||
        link.tags.some(tag => tag.toLowerCase().includes(term))
    );
};

export const getCategories = () => {
    return [...new Set(links.map(link => link.category))];
};

export const getAllTags = () => {
    const allTags = links.reduce((acc, link) => [...acc, ...link.tags], []);
    return [...new Set(allTags)];
};

// Exportar todo
export default {
    links,
    getLinksByCategory,
    getLinksByTag,
    searchLinks,
    getCategories,
    getAllTags
};